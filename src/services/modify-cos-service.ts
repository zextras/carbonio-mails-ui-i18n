export const modifyCos = async (body: JSON): Promise<any> =>
	fetch(`/service/admin/soap/ModifyCosRequest`, {
		method: 'POST',
		credentials: 'include',
		headers: {
			'Content-Type': 'application/json'
		},
		body: JSON.stringify({
			Body: {
				ModifyCosRequest: body
			}
		})
	});
